void attempt_apprentice(object ob)
{
        if (! permit_recruit(ob))
                return;

        if ((int)ob->query_int() < 30 )
        {
                command("say 这位"+RANK_D->query_respect(ob)+"恐怕资质差了一点。");
                command("say 不过尚大有潜力，还是先回去磨练磨练。");
                return ;
        }

        command("say " + ob->query("name") + "看你这么可怜，勉为其难收下你吧。\n");
        command("recruit " + ob->query("id"));
}

mixed ask_me()
{
        object me;

        me = this_player();
        if (me->query("can_perform/jiuyin-baiguzhao/shenzhao"))
                return "你还不快去好好的练习一下九阴白骨爪！";

        if (me->query("family/family_name") != "峨嵋派")
                return RANK_D->query_respect(me) + "不是我们峨嵋派的人，请走吧！";

        if (me->query_skill("jiuyin-baiguzhao", 1) < 150)
                return "你的九阴白骨爪那么差劲，别跟我开玩笑了！";

        message_vision(HIY "$n" HIY "面色一沉，右手成爪形飞击而出，"
                       "破空之气不绝于耳，丝丝冷风刮过你头顶！\n"
                       HIY "$n" HIY "回头凝视着$N" HIY "道：这一"
                       "招没什么稀奇，就是要心狠手辣！\n" NOR,
                       me, this_object());
        tell_object(me, HIG "你学会了九阴神爪。\n" NOR);
        me->set("can_perform/jiuyin-baiguzhao/shenzhao", 1);
        return 1;
}

